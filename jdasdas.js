// Operaciones matemáticas básicas

// Definir dos números para realizar las operaciones
const numero1 = 10;
const numero2 = 5;

// Suma
const suma = numero1 + numero2;
console.log(`Suma: ${numero1} + ${numero2} = ${suma}`);

// Resta
const resta = numero1 - numero2;
console.log(`Resta: ${numero1} - ${numero2} = ${resta}`);

// Multiplicación
const multiplicacion = numero1 * numero2;
console.log(`Multiplicación: ${numero1} * ${numero2} = ${multiplicacion}`);

// División
const division = numero1 / numero2;
console.log(`División: ${numero1} / ${numero2} = ${division}`);

// Residuo (Módulo)
const residuo = numero1 % numero2;
console.log(`Residuo: ${numero1} % ${numero2} = ${residuo}`);

// Raíz cuadrada
const raizCuadrada = Math.sqrt(numero1);
console.log(`Raíz cuadrada de ${numero1}: ${raizCuadrada}`);
